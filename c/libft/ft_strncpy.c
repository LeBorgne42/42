#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	if (n == 0)
		return (0);
	while (*src != '\0' && n > 0)
	{
		*dst++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*dst++ = '\0';
		n--;
	}
	return (dst);
}
