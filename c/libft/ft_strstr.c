#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	while (s2[i] != '\0')
	{
		if (s1[i] == '\0')
			return (0);
		if (s1[i] != s2[i])
			return (ft_strstr(s1 + 1, s2));
		i++;
	}
	return ((char*)s1);
}
