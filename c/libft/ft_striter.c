#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	c;
	int	i;

	if (s != NULL && f != NULL)
	{
		c = ft_strlen(s);
		i = 0;
		while (i < c)
		{
			(*f)(s);
			s++;
			i++;
		}
	}
}
